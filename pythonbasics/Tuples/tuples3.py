# Початковий індекс - включно, останній індекс - не включно

gas_volume = (153, 220, 0, 23, 120)

# Вивести перші два
print(gas_volume[0:2])

# Вивести 2 значення починаючи з другог елемента
print(gas_volume[1:3])

# Вивести всі значення починаючи з другог елемента
print(gas_volume[1:])

# Вивести всі значення до 3го елемента включно
print(gas_volume[:3])
